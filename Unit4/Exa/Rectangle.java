class Rectangle
{
	double x,y,width,height;
	void alterX(double x)
	{
		this.x=x;
	}
	void alterY(double y)
	{
		this.y=y;
	}
	void alterWidth(double width)
	{
		this.width=width;
	}
	void alterHeight(double height)
	{
		this.height=height;
	}
	double getX()
	{
		return x;
	}
	double getY()
	{
		return y;
	}
	double getWidth()
	{
		return width;
	}
	double getHeight()
	{
		return height;
	}
}
	